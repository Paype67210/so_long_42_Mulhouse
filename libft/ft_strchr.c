#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	while (*str && *str != (char)c)
		str++;
	if (*str == (char)c)
		return ((char *)str);
	if (!c && *str == '\0')
		return ((char *)str);
	return (NULL);
}
